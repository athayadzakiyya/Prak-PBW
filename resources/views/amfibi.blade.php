      Tentu kamu sudah tidak asing lagi dengan kodok yang satu ini kan? Hewan yang memiliki nama latin Buso sp ini adalah 
      salah satu jenis hewan amfibi ini bisa kamu jumpai di sawah, got, sungai, rawa, dan tempat lainnya. Kodok 
      memiliki kuku dan selaput pada kakinya yang digunakan untuk berenang saat hidup di dalam air. Kaki kodok yang panjang membuatnya memiliki kemampuan untuk melompat dengan jarak yang jauh.
      Hewan amfibi ini memiliki warna yang gelap, seperti hitam dan kecokelatan serta hidup di permukaan tanah. 
      Itulah mengapa kodok disebut sebagai hewan amfibi. Tak hanya itu, kodok memiliki lendir yang bercaun 
      pada bagian kulitnya yang kasar.</p>
