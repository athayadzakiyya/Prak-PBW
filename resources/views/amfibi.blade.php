      <p><h2>PENGERTIAN</h2>
      <div class="rectangle">
        Amfibi atau amfibia (Amphibia) merupakan kelas anggota dari Filum Chordata, 
        hewan bertulang belakang (vertebrata) hidup di air dan di daratan. Amfibi bertelur di air, 
        atau menyimpan telurnya di tempat yang lembap dan basah. Ketika menetas, larvanya yang hidup di air atau 
        tempat basah tersebut dan bernapas dengan insang. Setelah beberapa lama, larva tersebut kemudian berubah bentuk 
        (metamorfosis) menjadi hewan dewasa, yang umumnya hidup di daratan atau di tempat-tempat yang lebih kering dan 
        bernapas dengan paru-paru.</p>
        
        <p>Hewan berlendir dan berdarah dingin ini 
          dibagi dalam tiga ordo: Anura (katak), Caudata (salamander), Gymnophiona (Apoda).</p>
      </div>
       
