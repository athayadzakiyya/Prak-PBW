      di sawah yang tergenang air. Katak memiliki kaki dan berselaput yang membuatnya lihai berenang.Kulit katak sangat 
      licin dan memiliki warna yang tidak mencolok. Meski katak memiliki kulit yang licin, namun lendir yang dihasilkan tidak beracun seperti kodok.Katak bernafas 
      dengan insang saat masih menjadi kecebong, namun saat dewasa ia menggunakan sistem pernafasan invertebrate. Makanan kesukaan katak adalah serangga kecil yang ada di tepian sawah.</p>
